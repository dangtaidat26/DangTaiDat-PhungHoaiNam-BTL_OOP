package sample;

/* Lớp mô tả tọa độ trong bản đồ */
public class Point {
    int x;
    int y;

    /* Constructor */
    public Point(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    /* Getter */
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}

